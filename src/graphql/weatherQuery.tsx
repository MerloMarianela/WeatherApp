import gql from 'graphql-tag';

export const WEATHER_QUERY = gql`
  query getCityById($id: [String!]) {
    getCityById(id:$id){
      id
      name
      country
      coord {
        lon
        lat
      }
      weather {
        summary {
          title
          description
          icon
        }
        temperature {
          actual
          feelsLike
          min
          max
        }
        wind {
          speed
          deg
        }
        clouds {
          all
          visibility
          humidity
        }
        timestamp
      }
    }
  }
`;

